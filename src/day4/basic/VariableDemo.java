package day4.basic;

public class VariableDemo {
    public static void main(String[] args) {
        // single declaration
        int a ;
        // single initialization
         a = 5;

         // single declaration and initialization
        int b = 5;

        // Multiple declaration
        int c , d, e;

        // Multiple initialization
        c = 9;
        d = 10;
        e = 33;

        // Multiple declaration and initialization
        int p = 29, q = 34, r = 45;

        //multiple declaration
        int x,y,z;
        // with same value of initialization
        x = y = z = 10;




    }
}
